            // *IMPLEMENTE AQUI*
            if (DiasReservados >= 10)
            {
               decimal valorDesconto = valor * 10 / 100;
               valorDesconto = valor - valorDesconto;
               Console.WriteLine("Valor com desconto de 10% :" + valorDesconto);
               return valorDesconto;
            }
            return valor;
        }
